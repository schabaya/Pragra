package Practice;
public class FlowControlStatmnts {
    /*There are three types of flow control statements in java
                1. Selection/conditional Statements: based on a particular condition the selected block will executed.
                        if
                        if-else
                        else-if
                        switch
                2. Iteration/Looping statements: The same code will be repeated multiple times.
                        for
                        for-each
                        while
                        do-while
                3. Transfer/Jumping statements
                        break
                        continue
                        goto
                        try
                        return

      1. if : to take only one option
            syntax :
                if (cond){
                true body}
    */
    //if statement example
    public static void main(String[] args) {
        int x = 20, y = 10;
        if (x < y) {
            System.out.println("x is greater than y");
        }
        /*
        if -else: takes two options
        syntax:
        if (cond) {
        true body}
        else{
        false body}
        If statement is taking condition that must be boolean.

         */
    }

}
